    private Usuario btnIniciarSecionActionPerformed(java.awt.event.ActionEvent evt) {
        this.c = new Cuenta(250, 5254.75);
        this.u = new Usuario(1, "Luis", "12345", getC());
        
        String nombreUsuario = txtUsuario.getText();
        String passwordUsuario = txtPassword.getText();
        if (this.u.login(u, passwordUsuario, nombreUsuario)) {
            loginExitoso = true;
            this.c.setUsuario(u);
            this.setVisible(false);
            return getU();
        } else {
            JOptionPane.showMessageDialog(this, "El usuario o la contrase침a son incorrecto", "Acceso", 2);
        }

        return null;

    }

