    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        try {
            double cantidad = Double.parseDouble(txtDinero.getText());
            if (!u.getCuenta().esMayorCero(cantidad)) {
                JOptionPane.showMessageDialog(this, "La cantidad ingresada no es valida", "La cantidad es muy baja", 0);
            } else {
                u.getCuenta().setSaldo((u.getCuenta().depositar(cantidad)));
                lblSaldo.setText(u.getCuenta().getSaldo() + "");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "La cantidad es incorrecto", "Algo salio mal", 0);
        }

    }//GEN-LAST:event_btnDepositarActionPerformed

    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        try {
            double cantidad = Double.parseDouble(txtDinero.getText());
            if(!u.getCuenta().permitirRetirar(cantidad)){
                JOptionPane.showMessageDialog(this, "La cantidad es invalida", "Cantidad por debajo de los limites", 0);
                return;
            }
            if (u.getCuenta().permitirRetirar(cantidad)) {
                u.getCuenta().setSaldo((u.getCuenta().retirar(cantidad)));
                lblSaldo.setText(u.getCuenta().getSaldo() + "");
            } else {
                JOptionPane.showMessageDialog(this, "No tiene suficiente dinero para retirar $"+cantidad +" pesos", "Pobreza", 2);
            }
            System.out.println("Hola Mundo");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "La cantidad es incorrecta", "Algo salio mal", 0);
        }
    }//GEN-LAST:event_btnRetirarActionPerformed

